#!/bin/bash -e

# Build data.generated.js (loaded by index.html) from the committed per-version result
# files in results/ (produced by fetch-results.sh) and the query sets in queries/.
#
# Output has two consts:
#   const datasets = [{name, queries:[...]}, ...]   // in the QUERY_ORDER the result
#                                                    // arrays are concatenated in
#   const data     = [{version, actual_version, release_date, machine,
#                      load_time:{ds:sec}, data_size:{ds:bytes}, result:[[...]], source}]
#
# Keeping data out of index.html (as the main ClickBench page does with
# data.generated.js) keeps the page diffable and lets the data regenerate on its own.

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "${HERE}"

# The order the runner concatenates per-dataset query results into result[] (see
# QUERY_ORDER in run-version.sh). datasets[].queries line up with result[] by offset.
QUERY_ORDER="mgbench ssb hits uk ontime taxi coffeeshop tpch tpcds job"

OUT=data.generated.js

{
    echo '// Generated by generate-results.sh from results/*.json and queries/*.sql. Do not edit.'

    # datasets: name + its queries (one per non-empty line of queries/<name>.sql).
    echo -n 'const datasets = ['
    first=1
    for ds in ${QUERY_ORDER}; do
        [ "${first}" = 0 ] && echo -n ','
        first=0
        queries_json="$(jq -R -s 'split("\n") | map(select(length > 0))' "queries/${ds}.sql")"
        printf '\n{"name":"%s","queries":%s}' "${ds}" "${queries_json}"
    done
    echo ''
    echo '];'
    echo ''

    # data: one entry per version, oldest release first (ties broken by version).
    echo -n 'const data = ['
    first=1
    while IFS=$'\t' read -r _rd file; do
        [ -z "${file}" ] && continue
        >&2 echo "processing ${file}"
        [ "${first}" = 0 ] && echo -n ','
        first=0
        jq --compact-output ". += {\"source\": \"${file}\"}" "${file}"
    done < <(
        for f in results/*.json; do
            # A missing OR empty release_date must still yield a non-empty sort key: an empty
            # first field makes the line start with a tab, which `read -r _rd file` then strips
            # as leading IFS whitespace, shifting the filename into _rd and dropping the entry.
            printf '%s\t%s\n' "$(jq -r 'if (.release_date // "") == "" then "9999-99-99" else .release_date end' "${f}")" "${f}"
        done | sort -t$'\t' -k1,1 -k2,2V
    )
    echo ''
    echo '];'
} > "${OUT}"

>&2 echo "wrote ${OUT} ($(wc -c < "${OUT}") bytes)"
